        /// </summary>
        Casual2v2,
        /// <summary>
        /// Specifies the Boat Battle type.
        /// </summary>
        BoatBattle,
        /// <summary>
        /// Specifies the Boat Battle practise type.
        /// </summary>
        BoatBattlePractise,
        /// <summary>
        /// Specifies the River race player versus player type.
        /// </summary>
        RiverRacePvP,
        /// <summary>
        /// Specifies the River race Duel type.
        /// </summary>
        RiverRaceDuel,
        /// <summary>
        /// Specifies the River race Colosseum Duel type.
        /// </summary>
        RiverRaceDuelColosseum,
        /// <summary>
        /// Specifies an unknown type.
        /// </summary>
        Unknown
