        /// Specifies the player versus environment type.
        /// </summary>
        PvE,
        /// <summary>
        /// Specifies the Clanmate type.
        /// </summary>
        ClanMate,
        /// <summary>
        /// Specifies the Tournament type.
        /// </summary>
        Tournament,
        /// <summary>
        /// Specifies the player friendly type.
        /// </summary>
        Friendly,
        /// <summary>
        /// Specifies the survival type.
        /// </summary>
        Survival,
        /// <summary>
        /// Specifies the 2 versus 2 type.
        /// </summary>
        PvP2v2,
        /// <summary>
        /// Specifies the Clanmates 2 versus 2 type.
        /// </summary>
        ClanMate2v2,
        /// <summary>
        /// Specifies the Challenge type.
        /// </summary>
        Challenge,
        /// <summary>
        /// Specifies the Challenge 2 versus 2 type.
        /// </summary>
        Challenge2v2,
