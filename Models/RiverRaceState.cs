namespace ClashRoyaleAPI
{
    /// <summary>
    /// Specifies the Clash Royale RiverRace states.
    /// </summary>
    public enum RiverRaceState {
        /// <summary>
        /// Specifies the Clan not found state.
        /// </summary>
        ClanNotFound,
        /// <summary>
        /// Specifies the access denied state.
        /// </summary>
        AccessDenied,
        /// <summary>
        /// Specifies the matchmaking state.
        /// </summary>
        Matchmaking,
        /// <summary>
        /// Specifies the matched state.
        /// </summary>
        Matched,
        /// <summary>
        /// Specifies the full state.
        /// </summary>
        Full,
        /// <summary>
        /// Specifies the ended state.
        /// </summary>
        Ended
    };
}