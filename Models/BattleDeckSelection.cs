namespace ClashRoyaleAPI
{
    /// <summary>
    /// Specifies the Clash Royale Battle Deck selections.
    /// </summary>
    public enum BattleDeckSelection
    {
        /// <summary>
        /// Specifies the collection selection.
        /// </summary>
        Collection,
        /// <summary>
        /// Specifies the Draft selection.
        /// </summary>
        Draft,
        /// <summary>
        /// Specifies the competitive Draft selection.
        /// </summary>
        DraftCompetitive,
        /// <summary>
        /// Specifies the predefined selection.
        /// </summary>
        Predefined,
        /// <summary>
        /// Specifies the event Deck selection.
        /// </summary>
        EventDeck,
        /// <summary>
        /// Specifies the pick selection.
        /// </summary>
        Pick,
        /// <summary>
        /// Specifies the War Deck pick selection.
        /// </summary>
        WarDeckPick,
        /// <summary>
        /// Specifies an unknown selection.
        /// </summary>
        Unknown
    };
}